final class TempSummaryStatistics{
        double avgTemp=0.0;
        double devTemp=0.0;
        double minTemp=0.0;
        double maxTemp=0.0;

        TempSummaryStatistics(double avgTemp, double devTemp, double minTemp, double maxTemp){
            this.avgTemp = avgTemp;
            this.devTemp = devTemp;
            this.minTemp = minTemp;
            this.maxTemp = maxTemp;
        }

}

